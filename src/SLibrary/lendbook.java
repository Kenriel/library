    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nid = txtid.getText();
    BookItem bitem = (BookItem) txtbook.getSelectedItem();

    SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
    String issuedate = date_format.format(txtissuedate.getDate());

    SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
    String returndate = date_format1.format(txtrdate.getDate());

    try (Connection connection = con; // use the existing connection
            PreparedStatement pet = connection.prepareStatement("INSERT INTO lendbook (memberid, bookid, issuedate, returndate) VALUES (?, ?, ?, ?)")) {
        pet.setString(1, nid);
        pet.setInt(2, bitem.id);
        pet.setString(3, issuedate);
        pet.setString(4, returndate);

        int k = pet.executeUpdate();

        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Book Issued");

            txtid.setText("");
            txtbook.setSelectedIndex(1);
            txtmember.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    } catch (SQLException ex) {
        Logger.getLogger(lendbook.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    
    }//GEN-LAST:event_jButton1ActionPerformed

