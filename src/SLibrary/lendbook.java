    public void Load_Books() {
    try (PreparedStatement pet = con.prepareStatement("SELECT * FROM book");
         ResultSet rs = pet.executeQuery()) {

        txtbook.removeAllItems();

        while (rs.next()) {
            txtbook.addItem(new BookItem(rs.getInt(1), rs.getString(2)));
        }
    } catch (SQLException ex) {
        Logger.getLogger(lendbook.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    public void Issuebook_Load() {
    try (PreparedStatement pet = con.prepareStatement("SELECT l.id, m.name, l.issuedate, l.returndate " +
        "FROM lendbook l " +
        "JOIN member m ON l.memberid = m.id " +
        "JOIN book b ON l.bookid = b.id");
    ResultSet rs = pet.executeQuery()) {

    DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
    d.setRowCount(0);

    ResultSetMetaData rsd = rs.getMetaData();
    int c = rsd.getColumnCount();

    while (rs.next()) {
        Vector<Object> row = new Vector<>();

        for (int i = 1; i <= c; i++) {
            row.add(rs.getObject(i));
        }

        d.addRow(row);
    }
} catch (SQLException ex) {
    Logger.getLogger(lendbook.class.getName()).log(Level.SEVERE, null, ex);
}

}

            

