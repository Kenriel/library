    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();

        if (selectIndex == -1) {
            JOptionPane.showMessageDialog(this, "Select an member to delete.");
            return;
        }

        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

        int dialogResult = JOptionPane.showConfirmDialog(this, "Do you want to delete the selected member?", "Warning", JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {
            try {
                pet = con.prepareStatement("DELETE FROM member WHERE id = ?");
                pet.setInt(1, id);
                int k = pet.executeUpdate();

                if (k == 1) {
                    JOptionPane.showMessageDialog(this, "Member Deleted");
                    txtname.setText("");
                    txtaddress.setText("");
                    txtphone.setText("");
                    txtname.requestFocus();
                    Member_Load();
                } else {
                    JOptionPane.showMessageDialog(this, "Error");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

