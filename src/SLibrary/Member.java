    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();

        if (selectIndex == -1) {
            JOptionPane.showMessageDialog(this, "Select an member to update.");
            return;
        }

        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

        String name = txtname.getText();
        String address = txtaddress.getText();
        String phone = txtphone.getText();

        try {
            pet = con.prepareStatement("UPDATE member SET name=?, address=?, phone=? WHERE id=?");
            pet.setString(1, name);
            pet.setString(2, address);
            pet.setString(3, phone);
            pet.setInt(4, id);
            int k = pet.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Member Updated");
                txtname.setText("");
                txtaddress.setText("");
                txtphone.setText("");
                txtname.requestFocus();
                Member_Load();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }   
        
    }//GEN-LAST:event_jButton2ActionPerformed

